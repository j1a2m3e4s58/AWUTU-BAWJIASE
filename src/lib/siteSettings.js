const isPlainObject = (value) => value && typeof value === 'object' && !Array.isArray(value);

export const mergeSiteSettings = (defaults, incoming) => {
  if (!isPlainObject(incoming)) {
    return defaults;
  }

  const output = { ...defaults };

  Object.entries(incoming).forEach(([key, value]) => {
    if (isPlainObject(value) && isPlainObject(defaults[key])) {
      output[key] = mergeSiteSettings(defaults[key], value);
      return;
    }

    output[key] = value;
  });

  return output;
};

export const getMergedPublicSettings = (incoming) =>
  mergeSiteSettings(DEFAULT_PUBLIC_SETTINGS, incoming || {});
